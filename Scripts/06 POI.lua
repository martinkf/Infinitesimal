	for _, song in ipairs(inputArrayOfSongs) do
		local shouldAdd = false
		local songFirstTag = FetchFirstTag_POI(song)
		local songGenre = song:GetGenre()

		if inputListType == "SHORTCUT" and songFirstTag == inputListType then shouldAdd = true
		elseif inputListType == "FULLSONG" and songFirstTag == inputListType then shouldAdd = true
		elseif inputListType == "REMIX" and songFirstTag == inputListType then shouldAdd = true
		elseif inputListType == "ARCADE" and songFirstTag == inputListType then shouldAdd = true
		elseif inputListType == "ORIGINAL" and songGenre == inputListType then shouldAdd = true
		elseif inputListType == "KPOP" and songGenre == inputListType then shouldAdd = true
		elseif inputListType == "WORLDMUSIC" and songGenre == inputListType then shouldAdd = true
		end
	
		if shouldAdd then table.insert(reorderedSongs, song) end
	end

	output = reorderedSongs -- keep in mind that this could be still {}
	return output
end

-- takes: a string, from the possible ListOfPlaylists
-- returns: an array of Songs
-- based on: takes all the songs in the game, remove some to leave only the ones related to a PIU version playlist
function GetArrayOfSongsBasedOnPlaylist_POI(inputPlaylistAsString)
	local outputSongArray = {}
	
	if inputPlaylistAsString ~= "All Songs" then	
		-- loads up an array of strings, each containing the folder name of the songs "allowed in" this playlist
		local stringArrayOfFolderNamesToMatch = GetArrayOfStringsongdirFromPOINestedList_POI(GetPOINestedList_POI(inputPlaylistAsString))		

		-- Iterate through each folder name to match
		for _, folderNameToMatch in ipairs(stringArrayOfFolderNamesToMatch) do
			-- Iterate through all songs
			for _, song in ipairs(SONGMAN:GetAllSongs()) do
				-- Extract the folder name from the song's directory
				local folderName = song:GetSongDir()

				-- Check if the folder name matches the current folder name to match
				if string.find(folderName, folderNameToMatch, 1, true) then
					-- Add the song to the filtered array
					table.insert(outputSongArray, song)
				end
			end
		end
	else		
		outputSongArray = ReorderSongs_POI(SONGMAN:GetAllSongs())
	end
	
	return outputSongArray
end



-- ================================================================================================================= RETURNS AN ARRAY OF CHART OBJECTS 
-- takes: (1) the string related to the current group name
-- takes: (2) the song that's currently being selected by the music wheel
-- takes: (3) the array of Charts of the currently selected song
-- returns: an array of Charts
-- based on: takes into consideration the CurGroupName + which Song we're talking about to look up the playlist and filter out charts
function FilterChartFromGroup_POI(input_CurGroupName,input_CurrentSong,input_ChartArray)
	local outputChartArray = {}		
	outputChartArray = input_ChartArray
	
	local LocalListOfPlaylists = ListOfPlaylists_POI()
	
	local found = false
	local foundIndex = nil
	for i, playlist in ipairs(LocalListOfPlaylists) do
		if string.find(input_CurGroupName, playlist, 1, true) then			
            found = true
			foundIndex = i
            break -- break if found to avoid unnecessary iterations
        end
	end
	
	local playlistNestedList = {{}}
	if found then		
		-- Pass the matched string from LocalListOfPlaylists
        playlistNestedList = GetPOINestedList_POI(LocalListOfPlaylists[foundIndex])		
	else
		return input_ChartArray -- handle the case where the input group name doesn't have a corresponding playlist string
	end

	local currentSongDir = input_CurrentSong:GetSongDir()		
	-- Find the sublist corresponding to the current song
	local allowedDescriptions = {}
	for _, sublist in ipairs(playlistNestedList) do
		if sublist[1] == currentSongDir then
			-- Collect allowed descriptions
			allowedDescriptions = {unpack(sublist, 2)}
			break
		end
	end		
	
	-- Remove charts whose descriptions are not in the allowed list
	for i = #outputChartArray, 1, -1 do
		local description = outputChartArray[i]:GetDescription()
		if not table.find(allowedDescriptions, description) then
			table.remove(outputChartArray, i)
		end
	end
	
	if #outputChartArray == 0 then -- what happens if no chart can be found for song
		return {} -- this will return an empty ChartArray, and ChartDisplay.lua will repopulate it with all possible charts for this song "to avoid other crashes" in their words
	end
	
	return outputChartArray
end



-- ================================================================================================================= RETURNS A POI NESTED LIST 
-- takes: a string, from the possible ListOfPlaylists_POI
-- returns: a "POI Nested List" - list of lists containing songs + charts inside songs
-- based on: fetching from the hard-coded list of playlists
function GetPOINestedList_POI(inputPlaylistAsString)
    local playlistIndex = -1
    
    -- Find the index of the inputPlaylistAsString in ListOfPlaylists_POI
    for i, playlistName in ipairs(ListOfPlaylists_POI()) do
        if playlistName == inputPlaylistAsString then
            playlistIndex = i
            break
        end
    end
    
    -- If the playlist is found, return the corresponding POI Nested List
    if playlistIndex ~= -1 then
        return ListOfPOINestedLists_POI()[playlistIndex]
    else
        return {{}} -- Return an empty list if the playlist is not found
    end
end













